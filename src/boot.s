org 0x07C00
bits 16

jmp start

;; ============================================================================
;; Data
;; ============================================================================
msg_hello db "Istar: Hello World!", 0x0D, 0x0A, 0

;; ============================================================================
;; Function
;; ============================================================================
;; bios_print
;;      si (str)
;; ----------------------------------------------------------------------------
bios_print:
	lodsb
	cmp al, 0
	jz .end
	mov ah, 0x0E
	mov bx, 0x07
	int 0x10
	jmp bios_print
	.end:
		ret

;; ----------------------------------------------------------------------------
;; entry
;; ----------------------------------------------------------------------------

start:
	mov si, msg_hello
	call bios_print

	cli
	hlt

times 510 - ($ - $$) db 0x90
dw 0xAA55
